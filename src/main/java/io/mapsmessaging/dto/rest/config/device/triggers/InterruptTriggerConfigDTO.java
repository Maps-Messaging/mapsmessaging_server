package io.mapsmessaging.dto.rest.config.device.triggers;

import io.swagger.v3.oas.annotations.media.Schema;
import lombok.Data;
import lombok.EqualsAndHashCode;
import lombok.NoArgsConstructor;

@Data
@EqualsAndHashCode(callSuper=false)
@NoArgsConstructor
@Schema(description = "Interrupt Trigger Configuration DTO")
public class InterruptTriggerConfigDTO extends BaseTriggerConfigDTO {

  @Schema(description = "Address of the interrupt trigger", example = "1")
  protected int address;

  @Schema(
      description = "Pull direction of the interrupt trigger (e.g., UP or DOWN)",
      example = "UP")
  protected String pullDirection;

  @Schema(description = "Unique identifier for the trigger", example = "trigger1")
  protected String id;
}
